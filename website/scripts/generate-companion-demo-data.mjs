import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import emojibaseData from "emojibase-data/en/data.json" with { type: "json" };
import iamcalShortcodes from "emojibase-data/en/shortcodes/iamcal.json" with { type: "json" };
import {
  buildNativeEmojiSearchTermMapFromAdapter,
  buildShortcodeMapFromPreset,
  getShortcodes,
} from "@slithy/emoji-transforms";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outputPath = path.resolve(
  __dirname,
  "../src/components/docs/generated/companion-demo-data.ts",
);

const fullNativeShortcodes = buildShortcodeMapFromPreset(
  emojibaseData,
  iamcalShortcodes,
);
const fullNativeSearchTerms = buildNativeEmojiSearchTermMapFromAdapter(
  emojibaseData,
  {
    getEmoji: (entry) => entry.emoji,
    getShortcodes: (entry) =>
      entry.emoji ? getShortcodes(fullNativeShortcodes, entry.emoji) : [],
  },
);

const contents = `// Generated by website/scripts/generate-companion-demo-data.mjs
// Do not edit by hand.

export const fullNativeShortcodes = ${JSON.stringify(fullNativeShortcodes, null, 2)} as const;
export const fullNativeSearchTerms = ${JSON.stringify(fullNativeSearchTerms, null, 2)} as const;
`;

await fs.mkdir(path.dirname(outputPath), { recursive: true });
await fs.writeFile(outputPath, contents);
